/* - Write a C++ program to take length and breadth of a rectangle and print its area. (Easy)
Sample Input : 7, 4
Sample Output : 28  */
#include<iostream>
using namespace std;
int main(){
    int length,breadth;
    cout<<"Enter lenght and breadth of rectangle"<<endl;
    cin>>length>>breadth;
    cout<<"Area of rectangle is: "<<length*breadth;
    return 0;
}
