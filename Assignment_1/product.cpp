/* Take 2 integer values in two variables x and y and print their product. (Easy)
Sample Input : x=2, y=4 (Both integers)
Sample Output : 8 */
#include<iostream>
using namespace std;
int main(){
    int x,y;
    cout<<"Enter two numbers"<<endl;
    cin>>x>>y;
    cout<<"Product of "<<x<<" and "<<y<<" is: "<<x*y<<endl;
    return 0;
}
